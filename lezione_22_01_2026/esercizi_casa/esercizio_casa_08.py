print(f"8 % 1 = {8 % 1}")

potenza = 2 ** 3
print(f"Potenza: {potenza}")

divisione_intera = 8 // 3
print(f"Divisione intera: {divisione_intera}")

totale_acquisto = potenza + divisione_intera
print(f"Totale acquisto: {totale_acquisto}")

operazione_complessa = (8 - 1) + (2 ** 3) - (8 // 3)
print(f"Operazione complessa: {operazione_complessa}")