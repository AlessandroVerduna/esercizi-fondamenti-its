int = 5
float = 3.14

prodotto = int * float
print(f"Il prodotto di {int} e {float} è {prodotto}")
print(f"Tipo di prodotto: {type(prodotto)}")

risultato = float / int
print(f"Il risultato della divisione di {float} per {int} è {risultato}")
print(f"Tipo di risultato: {type(risultato)}")

sottrazione = float - int
print(f"La sottrazione di {float} meno {int} è {sottrazione}")
print(f"Tipo di sottrazione: {type(sottrazione)}")